export function getAcademyLesson(lessonId: string) {
  return academyLessons.find((lesson) => lesson.id === lessonId);
}

export function getLessonsByCategory(category: ChessAcademyCategory) {
  return academyLessons.filter((lesson) => lesson.category === category);
}
